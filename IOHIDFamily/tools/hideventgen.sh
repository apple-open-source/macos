function gen_file {
    FILE=${TARGET_DIR}/${2}
    echo "${HEADER}" > $FILE
    printf "\n//\n"                                                                   >> $FILE
    printf "// DO NOT EDIT THIS FILE. IT IS AUTO-GENERATED\n"                       >> $FILE
    printf "//\n\n"                                                                   >> $FILE
    echo "${2}" | awk '{ x=gsub(/\./,"_",$1); print "#ifndef _" toupper($x)}'       >> $FILE
    echo "${2}" | awk '{ x=gsub(/\./,"_",$1); print "#define _" toupper($x) "\n"}'  >> $FILE
    python ${SELF_DIR}/hideventdata.py  -t ${1} -f ${SELF_DIR}/hideventdata.json    >> $FILE
    echo "#endif"                                                                   >> $FILE
}

